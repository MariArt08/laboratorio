    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        reservas miReserva = new reservas();
        miReserva.setId( Integer.parseInt(txtId.getText()  )   );
        miReserva.setNumAsiento( txtAsiento.getText()  );
        miReserva.setEstado(txtEstado.getText());
        miReserva.setFkVuelo (Integer.parseInt(txtFkVuelo.getText()));
        miReserva.setFkPasajero (Integer.parseInt(txtFkPasajero.getText()));

        
        int indice = 0;
        
        try {
            reservas Consultada = Conexion.obtenerReservas( txtId.getText() );
            if (Consultada != null){
                indice = Consultada.getId();
            }
            else {
                indice = 0;
            }
        } catch (SQLException ex) {
            indice = 0;
        }
  
        //---Insertar o Modificar en la tabla
        if (indice == 0){
            try {
                if (Conexion.insertReservas(miReserva) != 0) {
                    JOptionPane.showMessageDialog(null, "Reserva Guardada" );
                }
                        
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.toString() );
            }
        }else{
           try {
                if (Conexion.updateReservas(miReserva) != 0) {
                    JOptionPane.showMessageDialog(null, "Reserva Modificada" );
                }
                        
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.toString() );
            }
        }
        btnConsultar.doClick();
        limpiar();
    }//GEN-LAST:event_btnGuardarActionPerformed

