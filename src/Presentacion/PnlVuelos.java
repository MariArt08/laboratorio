    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String ID="";
        if ( ID.equals (txtId.getText())){
            JOptionPane.showMessageDialog(null, 
                    "Debe digitar algo en el campo de Identificador",
                    "Mensaje importante", JOptionPane.WARNING_MESSAGE);
        }else {
            try {
                vuelos miVuelo = Conexion.obtenerVuelos(txtId.getText());
                txtNumVuelo.setText(miVuelo.getNumVuelo());
                txtOrigen.setText(miVuelo.getOrigen());
                txtDestino.setText(miVuelo.getDestino());
                txtFkAviones.setText(String.valueOf(miVuelo.getFkAvion()));
                dtpFecha.setDate(miVuelo.getFecha());
            } catch (SQLException ex) {
                Logger.getLogger(PnlVuelos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

