    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:
        String ID="";
        if ( ID.equals (txtId.getText())){
            JOptionPane.showMessageDialog(null, 
                    "Debe digitar algo en el campo de Identificador",
                    "Mensaje importante", JOptionPane.WARNING_MESSAGE);
        }else {
            try {
                if (Conexion.deleteVuelos(Integer.parseInt(txtId.getText())) != 0) {
                    JOptionPane.showMessageDialog(null, "Vuelo Borrado" );
                }
            } catch (SQLException ex) {
                Logger.getLogger(PnlVuelos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        btnConsultar.doClick();
        limpiar();
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        vuelos miVuelo = new vuelos();
        miVuelo.setId( Integer.parseInt(txtId.getText()  )   );
        miVuelo.setNumVuelo( txtNumVuelo.getText()  );
        miVuelo.setOrigen(txtOrigen.getText());
        miVuelo.setDestino(txtDestino.getText());
        miVuelo.setFecha(dtpFecha.getDate());
        miVuelo.setFkAvion (Integer.parseInt(txtFkAviones.getText()));

        
        int indice = 0;
        
        try {
            vuelos Consultada = Conexion.obtenerVuelos( txtId.getText() );
            if (Consultada != null){
                indice = Consultada.getId();
            }
            else {
                indice = 0;
            }
        } catch (SQLException ex) {
            indice = 0;
        }
  
        //---Insertar o Modificar en la tabla
        if (indice == 0){
            try {
                if (Conexion.insertVuelo(miVuelo) != 0) {
                    JOptionPane.showMessageDialog(null, "Vuelo Guardado" );
                }
                        
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.toString() );
            }
        }else{
           try {
                if (Conexion.updateVuelos(miVuelo) != 0) {
                    JOptionPane.showMessageDialog(null, "Vuelo Modificado" );
                }
                        
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.toString() );
            }
        }
        btnConsultar.doClick();
        limpiar();
    }//GEN-LAST:event_btnGuardarActionPerformed

